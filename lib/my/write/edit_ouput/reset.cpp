int reset_ouput(const int fd)
{
    int res = OK;

    res += putchar(fd, ESC);
    res += putstr(fd, "[0m");
    if (res != OK)
        return err_prog(UNDEF_ERR, KO, ERR_INFO);
    return OK;
}
